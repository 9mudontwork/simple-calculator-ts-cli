export class Calculator {
	plus(number1: number, number2: number) {
		return number1 + number2
	}

	minus(number1: number, number2: number) {
		return number1 - number2
	}

	multiply(number1: number, number2: number) {
		return number1 * number2
	}

	divide(number1: number, number2: number) {
		return number1 / number2
	}
}
